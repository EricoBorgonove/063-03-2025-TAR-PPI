#Faça um programa que entre com três números, faça a média aritmética e mostre o resultado.

numero1 = float (input ("Digite o 1 numero: "))
numero2 = float (input ("Digite o 2 numero: "))
numero3 = float (input ("Digite o 3 numero: "))

media_aritmetica = (numero1 + numero2 + numero3) / 3

print ("A média é ", media_aritmetica)